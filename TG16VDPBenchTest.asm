;*********************************************************
;* TG16VDPBenchmark.asm
;*
;* Program description:
;*   This program tests the performance of the NEC
;*   TurboGrafx-16 (TG16) Video Display Processor (VDP)
;*   using the following algorithm:
;*     1) Load and initalize the TG16 settings.
;*     2) Display 32x28 background tiles 4,608 times.
;*     4) Turn off the screen.
;*     5) Loop until TG16 is reset or turned off.
;*
;* Update History:
;*   Oct 09 2022 - First version. Hagop Mouradian
;*********************************************************

; Local constants.
VideoRegister      .equ $0000
VideoData          .equ $0002
VideoDataP1        .equ $0003
ColourControl      .equ $0400
ColourRegister     .equ $0402
ColourData         .equ $0404
TimerControl       .equ $0C01
IRQDisable         .equ $1402
IRQStatus          .equ $1403
SourceAddress      .equ $20EE
DestAddress        .equ $20F0
DestAddressP1      .equ $20F1
VDCStatusRegister  .equ $20F6
A_LowByte          .equ $20F8
A_HighByte         .equ $20F9
ColourLowByte      .equ $20FC
ColourHighByte     .equ $20FD
NUM_24             .equ $18
NUM_28             .equ $1C
NUM_32             .equ $20
NUM_36             .equ $24
NUM_128            .equ $80
NUM_255            .equ $FF
RAM_x2100          .equ $2100
RAM_x2101          .equ $2101
RAM_x2102          .equ $2102

;**************************************************************
;*
;* vreg Macro
;*
;* Description:
;*   Updates the video register.
;*
;**************************************************************
vreg	.macro
	 lda \1
	 sta <VideoRegister
	 sta VideoRegister
	.endm

;**************************************************************
;*
;* sta2b Macro
;*
;* Description:
;*   Stores two bytes in the specified address.
;*
;**************************************************************
sta2b	.macro
	 lda LOW_BYTE \1
	 sta LOW_BYTE \2
	 lda HIGH_BYTE \1
	 sta HIGH_BYTE \2
	.endm

;**************************************************************
;*
;* dec2b Macro
;*
;* Description:
;*   Decrements the specified 2-byte value by 1.
;*
;**************************************************************
dec2b	.macro
	 sec
	 lda \1
	 sbc #01
	 sta \1
	 lda \1+1
	 sbc #00
	 sta \1+1
	.endm

;**************************************************************
;*
;* vload Macro
;*
;* Description:
;*   Stores the source data in the specified VRAM address.
;*
;**************************************************************
vload	.macro 
	 .if (\# = 3)
	 	sta2b #\1,<DestAddress
	 	sta2b #\2,<SourceAddress
	 	sta2b #\3,<ColourLowByte
	 .else
	 	sta2b #VRAM(\1),<DestAddress
	 	sta2b #\1,<SourceAddress
	 	sta2b #\2,<ColourLowByte
	 .endif
	 	jsr LoadVRAM
	.endm

	; Set memory location for initialization subroutines.
	.bank $00
	.org $FE00
	.code

; Copy the data source to VRAM.
LoadVRAM:
	jsr SetVDCWrite
	ldy $00
LoopLoadVRAM:
	lda [SourceAddress],Y
	sta VideoData
	iny
	lda [SourceAddress],Y
	sta VideoDataP1
	iny
	bne LoopLoadVRAM
	inc <SourceAddress+1
	dec2b <ColourLowByte
	lda <ColourLowByte
	ora <ColourHighByte
	bne LoopLoadVRAM
	rts

; Set the VDC write address.
SetVDCWrite:
	vreg #$00
	lda <DestAddress
	sta VideoData
	lda <DestAddressP1
	sta VideoDataP1
	vreg #$02
	rts

; Reset system parameters.
SystemReset:
	sei
	csh
	cld
	lda #$FF
	tam #$00
	tax
	lda #$F8
	tam #$01
	txs
	lda VideoRegister
	lda #$07
	sta IRQDisable
	sta IRQStatus
	stz TimerControl
	st0 #$05
	st1 #$00
	st2 #$00
	stz <$00
	tii $2000,$2001,$1FFF
	bsr InitializeVDC
	lda #$05
	sta IRQDisable
	vreg #05
	st1 #$CC
	cli
	jmp StartProgram

; Initialize Video Display Controller.
InitializeVDC:
        sta2b #TableVDC,<SourceAddress
	ldy $00
LoopInitVDC:
	lda [SourceAddress],Y
	bmi ExitInitVDC
	iny
	sta <VideoRegister
	sta VideoRegister
	lda [SourceAddress],Y
	iny
	sta VideoData
	lda [SourceAddress],Y
	iny
	sta VideoDataP1
	bra LoopInitVDC
ExitInitVDC:
	lda #%00000100
	sta ColourControl
	rts

; Video Display Controller table definition.
TableVDC:
 	.db $05,$00,$00
	.db $06,$00,$00
	.db $07,$00,$00
	.db $08,$00,$00
	.db $0A,$02,$02
	.db $0B,$1F,$04
	.db $0C,$07,$0D
	.db $0D,$DF,$00
	.db $0E,$03,$00
	.db $0F,$10,$00
	.db $13,$00,$7F
	.db -1

VDCInterrupt:
	pha
	phx
	phy
	lda VideoRegister
	sta <VDCStatusRegister
	lda <VideoRegister
	sta VideoRegister
	ply
	plx
	pla
	rti

TimerInterrupt:
	sta IRQStatus
	stz TimerControl
LoopTimerInterrupt:
	rti
	.org $FFF6
	.dw LoopTimerInterrupt
	.dw VDCInterrupt
	.dw TimerInterrupt
	.dw LoopTimerInterrupt
	.dw SystemReset

	; Set program memory location.
	.list
	.mlist
	.code
	.bank $00
	.org $E000

StartProgram:
	; Reference the tile and palette memory banks, and load background tiles to VRAM.
	lda #bank(BGTile0)
	tam #page(BGTile0)
	vload $2000, BGTile0, $200
	lda #bank(Palette1)
	tam #page(Palette1)

	; Initialize the colour palette.
	lda #$00
	sta <A_LowByte
	sta2b #Palette1,<SourceAddress
	lda #$02
	sta <ColourLowByte
	lda <A_LowByte
	stz <A_HighByte
	asl A
	asl A
	asl A
	asl A
	rol <A_HighByte
	sta ColourRegister
	lda <A_HighByte
	sta ColourRegister+1
	ldy $00
LoopPalette1:
	ldx #$10
LoopPalette2:
	lda [SourceAddress],Y
	iny
	sta ColourData
	lda [SourceAddress],Y
	sta ColourData+1
	iny
	bne NextPalette
	inc <SourceAddress+1
NextPalette: 
	dex
	bne LoopPalette2
	dec <ColourLowByte
	bne LoopPalette1

	; Short delay before continuing.
	ldx #NUM_24
LoopDelay1:
	stx RAM_x2100
	ldx #NUM_255
LoopDelay2:
	ldy #NUM_255
LoopDelay3:
	dey
	bne LoopDelay3
	dex
	bne LoopDelay2
	ldx RAM_x2100
	dex
	bne LoopDelay1

	; Turn on screen.
	st0 #$05
	st1 #$CC
	st2 #$00

	; Initialize video settings.
	sta2b #$1200,<SourceAddress
	sta2b #$0000,<DestAddress
	vreg #$00
	lda <DestAddress
	sta VideoData
	lda <DestAddressP1
	sta VideoDataP1
	vreg #$02
	sec

	; Load 32*28 BG tiles for 128*36 times.
	ldy #NUM_36
Loop1:
	sty RAM_x2101
	ldx #NUM_128
Loop2:
	stx RAM_x2102
	ldx #NUM_28
Loop3:
	ldy #NUM_32
Loop4:
	sty SourceAddress
	lda LOW_BYTE <SourceAddress
	sta LOW_BYTE VideoData
	lda HIGH_BYTE <SourceAddress
	sta HIGH_BYTE VideoData
	dey
	bne Loop4 ; Loop 32->1
	dex
	bne Loop3 ; Loop 28->1
	vreg #$00
	ldx RAM_x2102
	dex
	bne Loop2 ; Loop 128->1
	ldy RAM_x2101
	dey
	bne Loop1 ; Loop 36->1

	; Turn off screen.
	st0 #$05
	st1 #$0C
	st2 #$00

LoopProgram:
	bra LoopProgram

	.bank $02
	.org $4000

